afterEvaluate {
    tasks.register("incrementVersion") {
        doLast {
            val counter = (project.property("version.counter") as String).toIntOrNull() ?: 0
            val newCounter = counter + 1
            val file = file("${rootProject.rootDir}/gradle.properties")
            val content = file.readText()
            val newContent = content.replace("version.counter=$counter", "version.counter=$newCounter")
            file.writeText(newContent)
            println("Version incremented to: 1.$newCounter")
        }
    }

    tasks.getByName("assembleDebug") {
        dependsOn("incrementVersion")
    }
}
